    function setLang(lang) {
        document.querySelectorAll('[data-key]').forEach(el => {
            const key = el.getAttribute('data-key');
            const val = langData[lang][key];
            if (val === undefined) return;
            if (el.tagName === 'INPUT' || el.tagName === 'TEXTAREA') {
                el.placeholder = val;
            } else {
                el.textContent = val;
            }
        });
        document.querySelectorAll('.lang-switcher button').forEach(btn => {
            btn.classList.toggle('active', btn.id === 'btn-' + lang || btn.id === 'btn-' + lang + '-d');
        });
    }

    const menuToggle = document.getElementById('menu-toggle');
    const drawerBackdrop = document.getElementById('drawer-backdrop');
    const drawer = document.getElementById('drawer');

    function closeNav() {
        document.body.classList.remove('nav-open');
        menuToggle.setAttribute('aria-expanded', 'false');
        drawer.setAttribute('aria-hidden', 'true');
        drawerBackdrop.setAttribute('aria-hidden', 'true');
        menuToggle.focus();
    }

    function openNav() {
        document.body.classList.add('nav-open');
        menuToggle.setAttribute('aria-expanded', 'true');
        drawer.setAttribute('aria-hidden', 'false');
        drawerBackdrop.setAttribute('aria-hidden', 'false');
    }

    function toggleNav() {
        if (document.body.classList.contains('nav-open')) closeNav();
        else openNav();
    }

    menuToggle.addEventListener('click', toggleNav);
    drawerBackdrop.addEventListener('click', closeNav);

    document.querySelectorAll('#drawer-nav a[href^="#"]').forEach(a => {
        a.addEventListener('click', () => {
            if (window.matchMedia('(max-width: 959px)').matches) closeNav();
        });
    });

    document.addEventListener('keydown', (e) => {
        if (e.key === 'Escape' && document.body.classList.contains('nav-open')) closeNav();
    });

    document.getElementById('newsletter-form').addEventListener('submit', function (e) {
        e.preventDefault();
        const input = this.querySelector('input[type="email"]');
        const thanks = document.getElementById('nl-thanks');
        if (!input.checkValidity()) {
            input.reportValidity();
            return;
        }
        thanks.classList.add('visible');
        this.reset();
        const active = document.querySelector('.site-header .lang-switcher button.active');
        const lid = active ? active.id : 'btn-ru';
        const lang = lid === 'btn-uz' || lid === 'btn-uz-d' ? 'uz' : lid === 'btn-en' || lid === 'btn-en-d' ? 'en' : 'ru';
        thanks.textContent = langData[lang].nl_thanks;
    });

    (function initReveal() {
        if (window.matchMedia('(prefers-reduced-motion: reduce)').matches) {
            document.querySelectorAll('.reveal').forEach(function (el) { el.classList.add('reveal-in'); });
            return;
        }
        var io = new IntersectionObserver(function (entries) {
            entries.forEach(function (entry) {
                if (entry.isIntersecting) {
                    entry.target.classList.add('reveal-in');
                    io.unobserve(entry.target);
                }
            });
        }, { threshold: 0.08, rootMargin: '0px 0px -24px 0px' });
        document.querySelectorAll('.reveal').forEach(function (el) { io.observe(el); });
    })();
